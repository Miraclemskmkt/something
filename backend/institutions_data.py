def group_by_region(
    college_type: str | None = None,
    region: str | None = None,
    search: str | None = None,
    tag: str | None = None,
) -> list[dict]:
    filtered = INSTITUTIONS
    if college_type:
        filtered = [i for i in filtered if i.college_type == college_type]
    if tag:
        filtered = [i for i in filtered if _matches_tag(i, tag)]
    if search:
        q = search.lower()
        filtered = [
            i for i in filtered
            if q in i.university.lower() or q in i.college.lower() or q in i.province
        ]

    regions: dict[str, dict] = {}
    for inst in filtered:
        r = inst.region
        if region and r != region:
            continue
        if r not in regions:
            regions[r] = {"region": r, "provinces": {}, "count": 0}
        prov = inst.province
        if prov not in regions[r]["provinces"]:
            regions[r]["provinces"][prov] = []
        regions[r]["provinces"][prov].append(inst)
        regions[r]["count"] += 1

    result = []
    for r in REGIONS_ORDER:
        if r in regions:
            data = regions[r]
            provinces = []
            for prov in sorted(data["provinces"].keys()):
                items = sorted(
                    data["provinces"][prov],
                    key=lambda x: (x.university, x.college),
                )
                provinces.append({"province": prov, "institutions": items, "count": len(items)})
            result.append({"region": r, "count": data["count"], "provinces": provinces})
    return result


def get_summary() -> dict:
    law = sum(1 for i in INSTITUTIONS if i.college_type == "law")
    foreign = sum(1 for i in INSTITUTIONS if i.college_type == "foreign_lang")
    universities = len({i.university for i in INSTITUTIONS})
    provinces = len({i.province for i in INSTITUTIONS})
    by_region = {r: 0 for r in REGIONS_ORDER}
    for i in INSTITUTIONS:
        by_region[i.region] = by_region.get(i.region, 0) + 1
    return {
        "total": len(INSTITUTIONS),
        "law_count": law,
        "foreign_lang_count": foreign,
        "university_count": universities,
        "province_count": provinces,
        "region_count": len(REGIONS_ORDER),
        "by_region": by_region,
        "tag_985": sum(1 for i in INSTITUTIONS if "985" in i.tags),
        "tag_211": sum(1 for i in INSTITUTIONS if "211" in i.tags),
        "tag_dfc": sum(1 for i in INSTITUTIONS if "双一流" in i.tags),
    }
